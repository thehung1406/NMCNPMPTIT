    public void jButton1ActionPerformed(ActionEvent e) {
            User user = new User();
            user.setUsername(jTextField1.getText());
            user.setPassword(jPasswordField1.getText());
            UserDAO ud = new UserDAO();
            if(ud.checkLogin(user)) {
                if(user.getRole().equalsIgnoreCase("Technician Manager")) {
                    (new TechnicianManganerHomeFrm(user)).setVisible(true);
                    this.dispose();
                }
                else
                    JOptionPane.showMessageDialog(this, "You do not allow to use");
            }else {
                JOptionPane.showMessageDialog(this, "Incorrect username and/or password!");
            }
    }
